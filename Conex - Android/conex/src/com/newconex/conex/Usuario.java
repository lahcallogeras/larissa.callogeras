package com.newconex.conex;

import java.util.Date;

public class Usuario {
	
	private int codigoUsu;
	private String usuarioUsu;
	private String senhaUsu;
	private String nomeUsu;
	private Date dataNascUsu;
	private int codigoPaisUsu;
	private String emailUsu;
	private String sexoUsu;
	private Date dataCadUsu;
	private String manterConUsu;
	private Date dataLoginUsu;
	private String nomeFotoUsu;	
	
	
	public int getCodigoUsu() {
		return codigoUsu;
	}
	public void setCodigoUsu(int codigoUsu) {
		this.codigoUsu = codigoUsu;
	}
	public String getUsuarioUsu() {
		return usuarioUsu;
	}
	public void setUsuarioUsu(String usuarioUsu) {
		this.usuarioUsu = usuarioUsu;
	}
	public String getSenhaUsu() {
		return senhaUsu;
	}
	public void setSenhaUsu(String senhaUsu) {
		this.senhaUsu = senhaUsu;
	}
	public String getNomeUsu() {
		return nomeUsu;
	}
	public void setNomeUsu(String nomeUsu) {
		this.nomeUsu = nomeUsu;
	}
	public Date getDataNascUsu() {
		return dataNascUsu;
	}
	public void setDataNascUsu(Date dataNascUsu) {
		this.dataNascUsu = dataNascUsu;
	}
	public int getCodigoPaisUsu() {
		return codigoPaisUsu;
	}
	public void setCodigoPaisUsu(int codigoPaisUsu) {
		this.codigoPaisUsu = codigoPaisUsu;
	}
	public String getEmailUsu() {
		return emailUsu;
	}
	public void setEmailUsu(String emailUsu) {
		this.emailUsu = emailUsu;
	}
	public String getSexoUsu() {
		return sexoUsu;
	}
	public void setSexoUsu(String sexoUsu) {
		this.sexoUsu = sexoUsu;
	}
	public Date getDataCadUsu() {
		return dataCadUsu;
	}
	public void setDataCadUsu(Date dataCadUsu) {
		this.dataCadUsu = dataCadUsu;
	}
	public String getManterConUsu() {
		return manterConUsu;
	}
	public void setManterConUsu(String manterConUsu) {
		this.manterConUsu = manterConUsu;
	}
	public Date getDataLoginUsu() {
		return dataLoginUsu;
	}
	public void setDataLoginUsu(Date dataLoginUsu) {
		this.dataLoginUsu = dataLoginUsu;
	}
	public String getNomeFotoUsu() {
		return nomeFotoUsu;
	}
	public void setNomeFotoUsu(String nomeFotoUsu) {
		this.nomeFotoUsu = nomeFotoUsu;
	}
	
}
