package com.zetta.pedaja;

public class ModelExcecaoProduto {
	
	private int ExcecaoTipoProdutoId;
	private int EmpresaId;
	private int TipoProdutoId;
	private int DiaSemana;
	private int HorarioInicial;
	private int HorarioFInal;
	private double Valor;
	
	
	public int getExcecaoTipoProdutoId() {
		return ExcecaoTipoProdutoId;
	}
	public void setExcecaoTipoProdutoId(int excecaoTipoProdutoId) {
		ExcecaoTipoProdutoId = excecaoTipoProdutoId;
	}
	public int getEmpresaId() {
		return EmpresaId;
	}
	public void setEmpresaId(int empresaId) {
		EmpresaId = empresaId;
	}
	public int getTipoProdutoId() {
		return TipoProdutoId;
	}
	public void setTipoProdutoId(int tipoProdutoId) {
		TipoProdutoId = tipoProdutoId;
	}
	public int getDiaSemana() {
		return DiaSemana;
	}
	public void setDiaSemana(int diaSemana) {
		DiaSemana = diaSemana;
	}
	public int getHorarioInicial() {
		return HorarioInicial;
	}
	
	public void setHorarioInicial(int horarioInicial) {
		HorarioInicial = horarioInicial;
	}
	public int getHorarioFInal() {
		return HorarioFInal;
	}
	public void setHorarioFInal(int horarioFInal) {
		HorarioFInal = horarioFInal;
	}
	public double getValor() {
		return Valor;
	}
	public void setValor(double valor) {
		Valor = valor;
	}
	
	
	
	

}
